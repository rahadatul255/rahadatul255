k = [k1 k2 k3 k4];
[m n]=size (p.MyData); %matriks m x n dengan ukuran sebanyak variabel x (input)
R=zeros (m,n); %membuat matriks R, yang merupakan matriks kosong
Y=zeros (m,n); %membuat matriks Y, yang merupakan titik kosong
for j=1:n,
 if k(j)==1, %statement untuk kriteria dengan atribut keuntungan
  R(:,j)=p.MyData(:,j)./max(p.MyData(:,j));
 else
  R(:,j)=min(p.MyData(:,j))./p.MyData(:,j);
 end;
end;
for i=1:m,
 V(i)= sum(w.*R(i,:));
end; 

hasil= max(V);
set(handles.edit18,'string',(hasil));%menampilkan output dari hasil

set(handles.text22,'string',(V(1)));
set(handles.text23,'string',(V(2)));
set(handles.text24,'string',(V(3)));
set(handles.text25,'string',(V(4)));



